/* StartDefaultTask function */
void StartDefaultTask(void const * argument)
{
	//This thread read every second the direction 0xd0 of the BMP280 sensor.
	//That sensor contains the chip ID

	//Almost with this Hall it's necessary to do like a first fail reading when you
	//initialize the sensor, the it works properly
	spiData[0]=0xD0 ; //
	HAL_GPIO_WritePin(GPIOA, GPIO_PIN_4, GPIO_PIN_RESET);//Chip Select Pin set at low level
	HAL_SPI_Transmit(&hspi1,spiData,1,10);//Transmit the register dir that you want to read
	HAL_SPI_Receive(&hspi1,&spiData[1],1,10);//Then receive from the sensor the data
	HAL_GPIO_WritePin(GPIOA, GPIO_PIN_4, GPIO_PIN_SET);// Chipe Select Pin set at high level


	for(;;)
	{
		HAL_GPIO_WritePin(GPIOA, GPIO_PIN_4, GPIO_PIN_RESET);
		HAL_SPI_Transmit(&hspi1,spiData,1,10);
		HAL_SPI_Receive(&hspi1,&spiData[1],1,10);
		HAL_GPIO_WritePin(GPIOA, GPIO_PIN_4, GPIO_PIN_SET);
		printf(" Return 0x%02X \r\n",spiData[1]);
		osDelay(1000);
	}

}

